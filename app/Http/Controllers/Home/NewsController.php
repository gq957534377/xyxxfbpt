     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $data = $request->all();
        if (empty($data['type']) || !in_array($data['type'], ['top', 'shehui', 'guonei', 'guoji', 'yule', 'tiyu', 'junshi', 'keji', 'caijing', 'shishang']))
            return view('errors.404');
        $host = "http://toutiao-ali.juheapi.com";
        $path = "/toutiao/index";
        $method = "GET";
        $appcode = "f75c1d38bc1b46c2a277c22237a385e6";
        $headers = array();
        array_push($headers, "Authorization:APPCODE " . $appcode);
        $querys = "type=" . $data['type'];
        $bodys = "";
        $url = $host . $path . "?" . $querys;

        $curl = curl_init();
        curl_setopt($curl, CURLOPT_CUSTOMREQUEST, $method);
        curl_setopt($curl, CURLOPT_URL, $url);
        curl_setopt($curl, CURLOPT_HTTPHEADER, $headers);
        curl_setopt($curl, CURLOPT_FAILONERROR, false);
        curl_setopt($curl, CURLOPT_RETURNTRANSFER, true);
        curl_setopt($curl, CURLOPT_HEADER, false);
        if (1 == strpos("$" . $host, "https://")) {
            curl_setopt($curl, CURLOPT_SSL_VERIFYPEER, false);
            curl_setopt($curl, CURLOPT_SSL_VERIFYHOST, false);
        }
        $news = json_decode(curl_exec($curl));
        return view('home.news.index', ['news' => $news, 'type' => $data['type']]);
    }

